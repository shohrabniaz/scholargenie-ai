#!/usr/bin/env node
/**
 * Prints production setup checklist and optionally appends CRON_SECRET to .env.local.
 * Usage: node scripts/setup-production.mjs
 */
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { randomBytes } from "node:crypto";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, "..");
const envPath = resolve(projectRoot, ".env.local");

if (existsSync(envPath)) {
  const content = readFileSync(envPath, "utf8");
  if (!content.includes("CRON_SECRET=")) {
    const secret = randomBytes(32).toString("hex");
    writeFileSync(
      envPath,
      `${content.trimEnd()}\n\n# Auto-generated by setup-production.mjs\nCRON_SECRET=${secret}\n`,
      "utf8",
    );
    console.log("Added CRON_SECRET to .env.local");
  }
}

console.log(`
ScholarGenie AI — production setup
==================================

1. Vercel (frontend)
   - Import https://github.com/shohrabniaz/scholargenie-ai
   - Add env vars from .env.example
   - Deploy — cron runs daily at 08:00 UTC (vercel.json)

2. Supabase → Authentication → URL Configuration
   - Site URL: https://YOUR-APP.vercel.app
   - Redirect: https://YOUR-APP.vercel.app/auth/callback

3. Optional integrations
   - OPENAI_API_KEY — GPT advisor
   - RESEND_API_KEY + RESEND_FROM_EMAIL — deadline emails
   - SUPABASE_SERVICE_ROLE_KEY — cron digest to all opted-in users

4. Database (if not migrated)
   npm run db:migrate
   npm run db:seed
   npm run db:seed-more
`);
